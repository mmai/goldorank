    <h3>Uninstall the extension from your browser ?</h3>
	<p>1. In the Firefox browser, click "Tools" and select "Extensions". The Extensions 
      window should appear. <br />
      2. Click "Goldorank", then click the "Uninstall" button.<br />
      3. Click the "OK" button in the confirmation window. The Extensions window 
      should confirm that Goldorank has been uninstalled. <br />
      4. Close the Extensions window, then close all Firefox windows and restart 
      the browser.</p>

