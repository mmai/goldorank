    <h3>Introduction</h3>
    <p>Goldorank est une extension pour le navigateur Firefox permettant de conna&icirc;tre de fa&ccedil;on rapide le positionnement d'une page ou d'une URL sur les principaux moteurs et annuaires. <span class="Style4">Si vous avez une version ant&eacute;rieure &agrave; la 0.8 : il est n&eacute;cessaire de la d&eacute;sinstaller avant d'installer la nouvelle version.</span></p>

	<div class="extension">
	  <h3 style="margin-top: 5px;">Installer Goldorank ? </h3>
	  <p style="clear: left;"><a href="goldorank.xpi"><img src="30x30.gif" alt="Extension" width="30" height="30" border="0" style="float: left; margin-right: 10px;" />goldorank.xpi</a> 
        <font color="#999999" size="1">(Firefox 1.5 ou +) </font></p>
	</div>
	

    <h3>Comment installer l'extension  ?</h3>
	<p>1.&nbsp;Cliquez sur le lien ci-dessus "goldorank.xpi".<br />
      <br />
      2. Si vous voyez ce message en haut de page du navigateur, cliquez sur &#8220;Options,&#8221; 
      &#8220;Autoriser,&#8221; &#8220;Fermer,&#8221; et recommen&ccedil;er l'installation de l'extension. <br />
      <img src="options.gif" width="739" height="29" /><br />
      <br />
    3. Red&eacute;marrer votre navigateur Firefox ainsi que toutes les fen&ecirc;tres ouverts en cours.<br>
	4. L'extension est  install&eacute;e. </p>  	
	
    <h3>Comment &ccedil;a marche ?</h3>
    <table width="100%" border="0" cellspacing="5" cellpadding="0">
      <tr>
        <td width="13%"><img src="scrren.gif" width="268" height="217"></td>
        <td width="87%" valign="top"><strong>- Clic droit </strong>sur la page pour activer <strong>Goldorank. </strong>.<br>
        <br>
        - Une fen&ecirc;tre apparait : vous pouvez &eacute;valuer le positionnement d'un mot cl&eacute; ou d'une phrase sur les principaux moteurs et annuaires. S&eacute;lectionnez les moteurs par pays et cliquez sur le bouton &quot;Search&quot;. <br>
        <br> 
        - Vous pouvez aussi s&eacute;lectionner un mot ou une phrase et par un simple <strong>clic droit</strong> lan&ccedil;er la recherche sur ce mot via la fen&ecirc;tre Goldorank. </td>
      </tr>
    </table>
    <h3>Comment d&eacute;sinstaller l'extension de votre navigateur?</h3>
	<p>1. Dans le menu du haut de votre navigateur cliquez sur "Outils" puis   "Extensions".<br />
      2. Choisissez "Goldorank", et cliquez sur d&eacute;sinstaller.<br />
      3. Confirmez l'op&eacute;ration : l'extension est d&eacute;sinstall&eacute;e. <br />
      4. Red&eacute;marrez votre navigateur. </p>

